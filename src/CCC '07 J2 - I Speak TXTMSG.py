while True:
    word = input()
    if word == "CU":
        print("see you".strip())
    elif word == ":-)":
        print("I'm happy".strip())
    elif word == ":-(":
        print("I'm unhappy".strip())
    elif word == ":-P":
        print("stick out my tongue".strip())
    elif word == "(~.~)":
        print("sleepy".strip())
    elif word == ";-)":
        print("wink".strip())
    elif word == "TA":
        print("totally awesome".strip())
    elif word == "CCC":
        print("Canadian Computing Competition".strip())
    elif word == "CUZ":
        print("because".strip())
    elif word == "TY":
        print("thank-you".strip())
    elif word == "YW":
        print("you're welcome".strip())
    elif word == "TTYL":
        print("talk to you later".strip())
        break
    else:
        print(word.strip())
